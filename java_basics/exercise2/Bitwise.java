public class Bitwise{
    public static void main(String Args[]){
        int a=10;
        int b=6;
//bitwise and
        System.out.println("a&b "+(a&b));
        //bitwise or
        System.out.println("a|b "+(a|b));

        //bitwise xor
        System.out.println("a^b "+(a^b));
//bitwise not
         System.out.println("~a = " + ~a);

         //bitwise left shitf
         System.out.println(a<<2);

        //bitwise right shift
        System.out.println(a>>2);

 
    }
}